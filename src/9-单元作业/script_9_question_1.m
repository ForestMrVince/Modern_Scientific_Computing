% 答: 由表格看出, 该问题应采用单元素方差分析.

clear; clc;
alpha = 0.02; % 置信度设置
% 使用函数: [p, tab, stats] = anova1(X)
% 构建 X 
X = [4.3, 6.1, 6.5, 9.3, 9.5;...
    7.8, 7.3, 8.3, 8.7, 8.8;...
    3.2, 4.2, 8.6, 7.2, 11.4;...
    6.5, 4.2, 8.2, 10.1, 7.8];

% 计算结果
% mean(X), 求X各列均值
[p, tab, stats] = anova1(X);

% 判定并输出
if p < alpha
    disp('染色工艺对缩水率存在显著影响！！！');
else
    disp('染色工艺对缩水率没有显著影响！！！');
end